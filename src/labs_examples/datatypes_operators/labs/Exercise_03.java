package labs_examples.datatypes_operators.labs;

/**
 * Please demonstrate the use of all arithmetic operators below. These include:
 *
 * addition, subtraction, multiplication, division and modulus
 *
 */
class ArithmeticOperators {

    public static void main(String[] args) {

        // write your code below
        int x = 10; int y = 20;
        int add = x + y;    //Addition
        System.out.println("x + y = " + add);
        int sub = y - x;    //Subtraction
        System.out.println("y - x = " + sub);
        int mul = x * y;    //Multiplication
        System.out.println("x * y = " + mul);
        int div = x / y;    //Division
        System.out.println("x / y = " + div);
        int mod = y % x;    //Modulus
        System.out.println("y % x = " + mod);


    }

}
